/**
 * Хук выполняется перед загрузкой основного содержимого списка товаров модуля Market.
 * Позволяет отключить статическое кеширование для страниц, где используются мультикатегории,
 * чтобы избежать «пустого белого экрана» из-за сохранения неконсистентного кеша.
 */

// Проверяем, что скрипт запущен в контексте Cotonti, иначе прерываем выполнение
defined('COT_CODE') or die('Wrong URL');

// Регистрируем пользовательскую таблицу мультикатегорий в объекте базы данных Cotonti,
// чтобы к ней можно было обращаться через $db->market_multicats даже до полной инициализации ядра
Cot::$db->registerTable('market_multicats');

// Объявляем глобальную переменную $c, которая содержит код текущей категории из GET-запроса
global $c;

// Если категория не выбрана (т.е. мы на главной странице модуля Market)
if (empty($c)) {
    // Проверяем, есть ли в таблице мультикатегорий хотя бы одна запись.
    // Если мультикатегории используются хотя бы для одного товара, кеш главной страницы
    // может стать некорректным после перехода из категории с мультикатегориями.
    $has_any_multi = (bool)Cot::$db->query(
        "SELECT 1 FROM {$db->market_multicats} LIMIT 1"
    )->fetchColumn();

    // Если мультикатегории используются, принудительно отключаем кеширование для главной страницы
    if ($has_any_multi) {
        Cot::$cache = false;
    }

    // Завершаем выполнение хука, потому что при отсутствии категории дальнейшая обработка не требуется
    return;
}

// Получаем экземпляр объекта базы данных Cotonti для выполнения запросов
$db = Cot::$db;

// Определяем числовой ID структуры (раздела) по коду текущей категории из таблицы структур.
// Используем безопасное экранирование значения категории и ограничиваем выборку областью 'market'.
// fetchColumn() возвращает первый столбец результата, который преобразуется в целое число.
$cat_id = (int)$db->query(
    "SELECT structure_id FROM {$db->structure}
     WHERE structure_code = " . $db->quote($c) . "
       AND structure_area = 'market'"
)->fetchColumn();

// Если структура с таким кодом не найдена или ID <= 0, дальнейшая обработка не имеет смысла
if ($cat_id > 0) {
    // Проверяем, существует ли хотя бы одна связь в таблице мультикатегорий для данной категории.
    // Это означает, что в этой категории отображаются товары, привязанные через множественные категории.
    $has_multi = (bool)$db->query(
        "SELECT 1 FROM {$db->market_multicats}
         WHERE pcat_cat_id = {$cat_id} LIMIT 1"
    )->fetchColumn();

    // Если категория содержит товары с мультикатегориями, отключаем статический кеш
    // чтобы страница всегда генерировалась динамически и не могла быть сохранена с некорректным состоянием
    if ($has_multi) {
        Cot::$cache = false;
    }
}
